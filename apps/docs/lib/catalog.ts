// Single source of truth for the component catalog. Nav, search, filters,
// cards, and component-page metadata all derive from this. Preview React
// components are mapped by `id` in app/_previews (kept separate so this module
// stays serializable and importable from Server Components).
import { product, installCommand } from "./product";

export type CategoryId =
  | "ai"
  | "developer-tools"
  | "collaboration"
  | "data-motion"
  | "mobile"
  | "commerce"
  | "file"
  | "security"
  | "communication"
  | "productivity"
  | "animated-shadcn"
  | "text"
  | "creative"
  | "backgrounds"
  | "product-backgrounds"
  | "workflow-heroes"
  | "icons";

export type Access = "free" | "pro";
export type AnimationEngine = "motion" | "css" | "motion+radix";
export type Complexity = "simple" | "medium" | "complex";
/** Lightweight production status (docs/39). Absent = legacy sellability track. */
export type ReleaseStatus = "released" | "experimental";

export interface CatalogItem {
  id: string;
  name: string;
  slug: string;
  description: string;
  category: CategoryId;
  subcategory?: string;
  status: "stable" | "beta";
  access: Access;
  dependencies: string[];
  registryDependencies: string[];
  tags: string[];
  /** Registry item name → generates the install command + registry URL. */
  registryItem: string;
  documentationPath: string;
  dateAdded: string; // ISO date
  featured: boolean;
  supportsDarkMode: boolean;
  supportsReducedMotion: boolean;
  requiresClient: boolean;
  animationEngine: AnimationEngine;
  /** Preferred preview stage proportion. */
  stage: "text" | "canvas" | "icon" | "interactive" | "card";
  /** Rapid-track metadata (docs/39). Optional for legacy items. */
  complexity?: Complexity;
  releaseStatus?: ReleaseStatus;
  /** Catalog entry kind. Absent = "component". Blocks/packs are labeled distinctly. */
  kind?: "component" | "block" | "pack";
  /** For blocks/packs: the component slugs they compose (shown on cards + pages). */
  composes?: string[];

  /* --- Catalog presentation overrides (docs/55). All optional; resolvePresentation()
     fills category/kind/complexity defaults when absent. Individual items override. --- */
  /** Desktop preview size taxonomy → drives height strategy + default span. */
  previewSize?: PreviewSize;
  /** How the catalog card renders the component. Detail pages are always interactive. */
  previewMode?: PreviewMode;
  /** Interior surface tone. Coherent card chrome shared; only the surface tone varies. */
  stageFamily?: StageFamily;
  /** 12-column desktop span. */
  cardSpan?: CardSpan;
}

/** Preview-size taxonomy (docs/55 §3). */
export type PreviewSize = "small" | "standard" | "wide" | "full" | "mobile" | "ambient";
/** Catalog cards use "staged"/"ambient"; detail pages use "interactive". */
export type PreviewMode = "staged" | "interactive" | "ambient";
/** Controlled interior surface tones (docs/55 §5) — not one style per component. */
export type StageFamily =
  | "ai"
  | "console"
  | "collab"
  | "data"
  | "mobile"
  | "commerce"
  | "security"
  | "productivity"
  | "creative"
  | "editorial"
  | "neutral";
export type CardSpan = 4 | 6 | 8 | 12;

export interface Presentation {
  previewSize: PreviewSize;
  previewMode: PreviewMode;
  stageFamily: StageFamily;
  cardSpan: CardSpan;
  /** Mobile always collapses to a single full-width column. */
  mobileCardSpan: 12;
}

