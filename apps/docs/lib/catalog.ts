// ---- derived helpers (used by nav/search/filters/cards/pages) ----

export const bySlug = new Map(catalog.map((c) => [c.slug, c]));

export function categoryCount(id: CategoryId): number {
  return catalog.filter((c) => c.category === id).length;
}

export function itemsByCategory(id: CategoryId): CatalogItem[] {
  return catalog.filter((c) => c.category === id);
}

export function featuredItems(): CatalogItem[] {
  return catalog.filter((c) => c.featured);
}

/** Entry-kind helpers. Absent `kind` means a plain component. */
export const kindOf = (c: CatalogItem): "component" | "block" | "pack" => c.kind ?? "component";
export function componentItems(): CatalogItem[] {
  return catalog.filter((c) => kindOf(c) === "component");
}
export function blockItems(): CatalogItem[] {
  return catalog.filter((c) => kindOf(c) === "block");
}
export function packItems(): CatalogItem[] {
  return catalog.filter((c) => kindOf(c) === "pack");
}

export function recentItems(limit = 8): CatalogItem[] {
  return [...catalog].sort((a, b) => b.dateAdded.localeCompare(a.dateAdded)).slice(0, limit);
}

/** Lightweight search over name/description/tags/category. */
export function searchCatalog(query: string): CatalogItem[] {
  const q = query.trim().toLowerCase();
  if (!q) return catalog;
  return catalog.filter((c) =>
    [c.name, c.description, c.category, ...c.tags].join(" ").toLowerCase().includes(q),
  );
}

export function itemInstall(item: CatalogItem): string {
  return installCommand(item.registryItem);
}

